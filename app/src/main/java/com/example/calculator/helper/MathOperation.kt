class MathOperation {
    companion object{
        fun addition(rightSide : Double, leftSide : Double) : Double{
            return rightSide + leftSide
        }

        fun substraction(rightSide : Double, leftSide : Double) : Double{
            return rightSide - leftSide
        }

        fun multiple(rightSide : Double, leftSide : Double) : Double{
            return rightSide * leftSide
        }

        fun divider(rightSide : Double, leftSide : Double) : Double{
            return rightSide / leftSide
        }
    }
}